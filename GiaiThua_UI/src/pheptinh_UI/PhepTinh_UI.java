    private void btnSumbitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumbitActionPerformed
        // TODO add your handling code here:
        if(IsNumber(txtInput1)&&IsNumber(txtInput2)){
            int n1 = Integer.parseInt(txtInput1.getText());
            int n2 = Integer.parseInt(txtInput2.getText());
            char n3 = cboToanTu.getSelectedItem().toString().charAt(0);
            PhepTinh pt = new PhepTinh(n1, n2, n3);
            txtResult.setText(String.valueOf(pt.tinhToan()));
        } else if(IsNumber(txtInput1))
            txtResult.setText("Input 2 invalid!");
        else
            txtResult.setText("Input 1 invalid!");
}//GEN-LAST:event_btnSumbitActionPerformed

