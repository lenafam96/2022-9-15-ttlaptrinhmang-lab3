    private boolean IsNumber(JTextField txtInput){
            String input = txtInput.getText();
            for(int i=0;i<input.length();i++){
                if(input.charAt(i)==45 && i==0)
                    continue;
                if(input.charAt(i)>57 || input.charAt(i)<48)
                    return false;
            }
            return txtInput.getText().length()>0;
        }
